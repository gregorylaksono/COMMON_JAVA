package de.act.common.system;

/**
 * Interface returned by instances of the <code>LockSet</code> class.
 *
 * @author OB
 * @see de.act.common.system.LockSet
 */
public interface Lock {

	/**
	 * Release the acquired lock.
	 * This gives other threads a chance to get a lock.
	 */
	void unlock();

} // interface Lock
