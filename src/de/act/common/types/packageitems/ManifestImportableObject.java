package de.act.common.types.packageitems;

import de.act.common.types.ManifestObject;

/**
 * @author Martin Sachs
 * @since 1.0 - 19.06.2006
 */
public abstract class ManifestImportableObject implements ManifestObject  {

	private static final long serialVersionUID = 4564227627187530930L;
	private boolean importObject = false;
	
	/* (non-Javadoc)
	 * @see de.act.common.types.ManifestObject#setImport(boolean)
	 */
	public void setImport(boolean isimport) {
		this.importObject = isimport;
	}

	public boolean isImport() {
		return this.importObject;
	}
}
