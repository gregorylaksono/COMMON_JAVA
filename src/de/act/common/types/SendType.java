package de.act.common.types;

/**
 * @author Martin Sachs
 * 
 */
public enum SendType {

	LOCAL("c"),
	CENTRAL("s"), 
	LOCAL_MANIFEST("c"),
	MANIFEST("s"),
	LOCAL_LOADINGLIST("c"),
	LOADINGLIST("c"), 
	BOOKING_REQUEST("s");
	private String c;

	private SendType(final String c) {
		this.c = c;
	}

	public String getAttCode() {
		return this.c;
	}
}
