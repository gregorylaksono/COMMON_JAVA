package de.act.common.types.views;

public class AccountingInformation implements java.io.Serializable {
	
	private String acc_id;
	private Integer acc_seq;
	private String acc_remark;
	private String acc_name;
	
	public AccountingInformation(){		
	}
	public String getAcc_id() {
		return acc_id;
	}
	public void setAcc_id(String acc_id) {
		this.acc_id = acc_id;
	}
	public Integer getAcc_seq() {
		return acc_seq;
	}
	public void setAcc_seq(Integer acc_seq) {
		this.acc_seq = acc_seq;
	}
	public String getAcc_remark() {
		return acc_remark;
	}
	public void setAcc_remark(String acc_remark) {
		this.acc_remark = acc_remark;
	}
	
	public String getAcc_name() {
		return acc_name;
	}
	public void setAcc_name(String acc_name) {
		this.acc_name = acc_name;
	}
}
