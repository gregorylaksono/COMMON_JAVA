package de.act.common.types.ruleobject;

/**
 * @author Henry
 */
public class RCharges implements java.io.Serializable {

	private static final long serialVersionUID = -898833305767538841L;
	private long RId;
	private RBase RBase;
	private Integer chargesBool;
	private String chargesType;

	public RCharges() {
		
	}

	public RCharges(RBase RBase) {
		this.RBase = RBase;
	}

	public RCharges(RBase RBase, Integer chargesBool, String chargesType) {
		this.RBase = RBase;
		this.chargesBool = chargesBool;
		this.chargesType = chargesType;
	}

	public long getRId() {
		return this.RId;
	}

	public void setRId(long RId) {
		this.RId = RId;
	}

	public RBase getRBase() {
		return this.RBase;
	}

	public void setRBase(RBase RBase) {
		this.RBase = RBase;
	}

	public Integer getChargesBool() {
		return this.chargesBool;
	}

	public void setChargesBool(Integer chargesBool) {
		this.chargesBool = chargesBool;
	}

	public String getChargesType() {
		return this.chargesType;
	}

	public void setChargesType(String chargesType) {
		this.chargesType = chargesType;
	}

}
