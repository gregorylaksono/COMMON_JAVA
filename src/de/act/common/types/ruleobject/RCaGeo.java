package de.act.common.types.ruleobject;

/**
 * @author Henry
 */
public class RCaGeo implements java.io.Serializable {

	private static final long serialVersionUID = -3804787641785744412L;
	private long RId;
	private RBase RBase;
	private Integer caGeoBool;
	private String caGeoSect;
	private String caGeoCa;
	private Short caGeoAlliance;
	private String caGeoIataStat;
	private Long caGeoFrom;
	private Long caGeoTo;

	public RCaGeo() {
		
	}

	public RCaGeo(RBase RBase) {
		this.RBase = RBase;
	}

	public RCaGeo(RBase RBase, Integer caGeoBool, String caGeoSect, String caGeoCa, Short caGeoAlliance, String caGeoIataStat, Long caGeoFrom, Long caGeoTo) {
		this.RBase = RBase;
		this.caGeoBool = caGeoBool;
		this.caGeoSect = caGeoSect;
		this.caGeoCa = caGeoCa;
		this.caGeoAlliance = caGeoAlliance;
		this.caGeoIataStat = caGeoIataStat;
		this.caGeoFrom = caGeoFrom;
		this.caGeoTo = caGeoTo;
	}

	public long getRId() {
		return RId;
	}

	public void setRId(long id) {
		RId = id;
	}

	public RBase getRBase() {
		return RBase;
	}

	public void setRBase(RBase base) {
		RBase = base;
	}

	public Integer getCaGeoBool() {
		return caGeoBool;
	}

	public void setCaGeoBool(Integer caGeoBool) {
		this.caGeoBool = caGeoBool;
	}

	public String getCaGeoSect() {
		return caGeoSect;
	}

	public void setCaGeoSect(String caGeoSect) {
		this.caGeoSect = caGeoSect;
	}

	public String getCaGeoCa() {
		return caGeoCa;
	}

	public void setCaGeoCa(String caGeoCa) {
		this.caGeoCa = caGeoCa;
	}

	public Short getCaGeoAlliance() {
		return caGeoAlliance;
	}

	public void setCaGeoAlliance(Short caGeoAlliance) {
		this.caGeoAlliance = caGeoAlliance;
	}

	public String getCaGeoIataStat() {
		return caGeoIataStat;
	}

	public void setCaGeoIataStat(String caGeoIataStat) {
		this.caGeoIataStat = caGeoIataStat;
	}

	public Long getCaGeoFrom() {
		return caGeoFrom;
	}

	public void setCaGeoFrom(Long caGeoFrom) {
		this.caGeoFrom = caGeoFrom;
	}

	public Long getCaGeoTo() {
		return caGeoTo;
	}

	public void setCaGeoTo(Long caGeoTo) {
		this.caGeoTo = caGeoTo;
	}
}
