package de.act.common.types.ruleobject;

/**
 * @author Henry
 */
public class RUld implements java.io.Serializable {

	private static final long serialVersionUID = -8960190793973804595L;
	private long RId;
	private RBase RBase;
	private Integer uldBool;
	private String uldClass;
	private String uldRate;

	public RUld() {
		
	}

	public RUld(RBase RBase) {
		this.RBase = RBase;
	}

	public RUld(RBase RBase, Integer uldBool, String uldClass, String uldRate) {
		this.RBase = RBase;
		this.uldBool = uldBool;
		this.uldClass = uldClass;
		this.uldRate = uldRate;
	}

	public long getRId() {
		return this.RId;
	}

	public void setRId(long RId) {
		this.RId = RId;
	}

	public RBase getRBase() {
		return this.RBase;
	}

	public void setRBase(RBase RBase) {
		this.RBase = RBase;
	}

	public Integer getUldBool() {
		return this.uldBool;
	}

	public void setUldBool(Integer uldBool) {
		this.uldBool = uldBool;
	}

	public String getUldClass() {
		return this.uldClass;
	}

	public void setUldClass(String uldClass) {
		this.uldClass = uldClass;
	}

	public String getUldRate() {
		return this.uldRate;
	}

	public void setUldRate(String uldRate) {
		this.uldRate = uldRate;
	}
}