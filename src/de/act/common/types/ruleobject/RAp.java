package de.act.common.types.ruleobject;

/**
 * @author Henry
 */
public class RAp implements java.io.Serializable {

	private static final long serialVersionUID = -5311395422066348118L;
	private long RId;
	private RBase RBase;
	private Integer apBool;
	private String ap3lc;
	private String apSect;

	public RAp() {
		
	}

	public RAp(RBase RBase) {
		this.RBase = RBase;
	}

	public RAp(RBase RBase, Integer apBool, String ap3lc, String apSect) {
		this.RBase = RBase;
		this.apBool = apBool;
		this.ap3lc = ap3lc;
		this.apSect = apSect;
	}

	public long getRId() {
		return this.RId;
	}

	public void setRId(long RId) {
		this.RId = RId;
	}

	public RBase getRBase() {
		return this.RBase;
	}

	public void setRBase(RBase RBase) {
		this.RBase = RBase;
	}

	public Integer getApBool() {
		return this.apBool;
	}

	public void setApBool(Integer apBool) {
		this.apBool = apBool;
	}

	public String getAp3lc() {
		return this.ap3lc;
	}

	public void setAp3lc(String ap3lc) {
		this.ap3lc = ap3lc;
	}

	public String getApSect() {
		return this.apSect;
	}

	public void setApSect(String apSect) {
		this.apSect = apSect;
	}

}
