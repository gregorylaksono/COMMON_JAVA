package de.act.common.types.staticobjects;

public class RSCourierCode implements java.io.Serializable {

	private static final long serialVersionUID = 2749021061386247756L;
	private String courier_route_id;
	private String courier_name;
	private String co_2lc;
	private String district;
	private String sub_district;
	private Integer postal_code;
	private RSVad vad;
	
	public String getCourier_route_id() {
		return courier_route_id;
	}
	public void setCourier_route_id(String courier_route_id) {
		this.courier_route_id = courier_route_id;
	}
	public String getCourier_name() {
		return courier_name;
	}
	public void setCourier_name(String courier_name) {
		this.courier_name = courier_name;
	}
	public String getCo_2lc() {
		return co_2lc;
	}
	public void setCo_2lc(String co_2lc) {
		this.co_2lc = co_2lc;
	}
	public String getDistrict() {
		return district;
	}
	public void setDistrict(String district) {
		this.district = district;
	}
	public String getSub_district() {
		return sub_district;
	}
	public void setSub_district(String sub_district) {
		this.sub_district = sub_district;
	}
	public Integer getPostal_code() {
		return postal_code;
	}
	public void setPostal_code(Integer postal_code) {
		this.postal_code = postal_code;
	}
	public RSVad getVad() {
		return vad;
	}
	public void setVad(RSVad vad) {
		this.vad = vad;
	}
}
