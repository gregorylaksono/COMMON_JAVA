package de.act.common.types.nonstaticobjects;

import java.io.Serializable;
import java.util.Date;

import de.act.common.types.staticobjects.RSState;

/**
 * @author Henry
 *
 */
public class AWBPrint implements Serializable{

	private static final long serialVersionUID = -4310903398735789721L;
	private Long id;
	private String f_id;
	private Integer creatorId;
	private RSState state_id;
	private String stat_descr;
	private Integer fwb_sent;
	private Date corr = new Date();

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getF_id() {
		return f_id;
	}

	public void setF_id(String f_id) {
		this.f_id = f_id;
	}

	public Integer getCreatorId() {
		return creatorId;
	}

	public void setCreatorId(Integer creatorId) {
		this.creatorId = creatorId;
	}

	public RSState getState_id() {
		return state_id;
	}

	public void setState_id(RSState state_id) {
		this.state_id = state_id;
	}

	public String getStat_descr() {
		return stat_descr;
	}

	public void setStat_descr(String stat_descr) {
		this.stat_descr = stat_descr;
	}

	public Date getCorr() {
		return corr;
	}

	public void setCorr(Date corr) {
		this.corr = corr;
	}

	public Integer getFwb_sent()
	{
		return fwb_sent;
	}

	public void setFwb_sent(Integer fwb_sent)
	{
		this.fwb_sent = fwb_sent;
	}
	
}
