package de.act.common.types.handling;

import java.io.Serializable;
import java.util.Date;

import de.act.central.types.AwbNo;

/**
 * @author Rony
 * @20140320
 */
public class AwbEPaymentView implements Serializable{

	private static final long serialVersionUID = 5516804547939436487L;
	
	private Long attId;
	private String fid;
	private String attType;
	private Short attNo;
	private Integer addId;
	private String auth;
	private String sign;
	private Date corr;	
	private AwbEPayment awbEPayment;
	private AwbNo awbNo;
	
	public long getAttId() {
		return attId;
	}
	
	public void setAttId(Long attId) {
		this.attId = attId;
	}	
	
	public String getFid() {
		return fid;
	}
	
	public void setFid(String fid) {
		this.fid = fid;
	}
	
	public String getAttType() {
		return attType;
	}
	
	public void setAttType(String attType) {
		this.attType = attType;
	}
	
	public Short getAttNo() {
		return attNo;
	}
	
	public void setAttNo(Short attNo) {
		this.attNo = attNo;
	}	
	
	public Integer getAddId() {
		return addId;
	}
	
	public void setAddId(Integer addId) {
		this.addId = addId;
	}
	
	
	public String getAuth() {
		return auth;
	}
	
	public void setAuth(String auth) {
		this.auth = auth;
	}
	
	public String getSign() {
		return sign;
	}
	
	public void setSign(String sign) {
		this.sign = sign;
	}	
	
	
	public Date getCorr() {
		return corr;
	}
	
	public void setCorr(Date corr) {
		this.corr = corr;
	}		
	
	public AwbEPayment getAwbEPayment() {
		return awbEPayment;
	}	
	
	public void setAwbEPayment(AwbEPayment awbEPayment) {
		this.awbEPayment = awbEPayment;
	}
	
	public AwbNo getAwbNo() {
		return awbNo;
	}
	
	public void setAwbNo(AwbNo awbNo) {
		this.awbNo = awbNo;
	}
}