package de.act.common.jmstypes.beanwrapper;

import java.io.Serializable;
import java.util.List;

import de.act.common.types.staticobjects.RSProvision;

public class JMSProvisionsObject implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -4710673340491705969L;
	private int blackboxID;
	private String state;
	private List<RSProvision> provisions;
	
	public int getBlackboxID() {
		return blackboxID;
	}
	public void setBlackboxID(int blackboxID) {
		this.blackboxID = blackboxID;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public List<RSProvision> getProvisions() {
		return provisions;
	}
	public void setProvisions(List<RSProvision> provisions) {
		this.provisions = provisions;
	}

}
