import java.io.Serializable;
import java.util.List;

import de.act.common.types.User;
import de.act.common.types.views.NewExportAcceptanceView;

/**
 * @author Henry
 *
 */
public class JMSTrigger implements Serializable
{
	private static final long serialVersionUID = 6415884391995321552L;
	
	private Integer blackboxID;
	private String state;
	private User user;
	private List<NewExportAcceptanceView> listToDrop;
	
	public Integer getBlackboxID() {
		return blackboxID;
	}
	
	public void setBlackboxID(Integer blackboxID) {
		this.blackboxID = blackboxID;
	}
	
	public String getState() {
		return state;
	}
	
	public void setState(String state) {
		this.state = state;
	}

	public User getUser() {
		return user;
	}

	public void setUser(User user) {
		this.user = user;
	}
	
	public List<NewExportAcceptanceView> getListToDrop() {
		return listToDrop;
	}

	public void setListToDrop(List<NewExportAcceptanceView> listToDrop) {
		this.listToDrop = listToDrop;
	}
}
