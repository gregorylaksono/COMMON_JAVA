package de.act.common.jmstypes.beanwrapper;

import java.io.Serializable;
import java.util.List;

import de.act.common.types.UserObject;
import de.act.common.types.staticobjects.RSAdd;
import de.act.common.types.views.UserAuthorityView;

/**
 * @author Henry
 *
 */
public class JMSPreferenceAddressObject implements Serializable{

	private static final long serialVersionUID = -5397720850345600085L;
	private RSAdd sadd;
	private Integer add_id;
	private String state;
	private int blackboxID;
	private UserObject userObject;
	private List<UserAuthorityView> listUserAuth;

	public RSAdd getSadd() {
		return sadd;
	}

	public void setSadd(RSAdd sadd) {
		this.sadd = sadd;
	}

	public Integer getAdd_id() {
		return add_id;
	}

	public void setAdd_id(Integer add_id) {
		this.add_id = add_id;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public int getBlackboxID() {
		return blackboxID;
	}

	public void setBlackboxID(int blackboxID) {
		this.blackboxID = blackboxID;
	}

	public UserObject getUserObject() {
		return userObject;
	}

	public void setUserObject(UserObject userObject) {
		this.userObject = userObject;
	}

	public List<UserAuthorityView> getListUserAuth() {
		return listUserAuth;
	}

	public void setListUserAuth(List<UserAuthorityView> listUserAuth) {
		this.listUserAuth = listUserAuth;
	}
}
