package de.act.common.jmstypes.beanwrapper;

import java.io.Serializable;
import java.util.List;

import de.act.common.types.nonstaticobjects.Preadvise;

/**
 * @author Henry
 *
 */
public class JMSSCCHandling implements Serializable{

	private static final long serialVersionUID = -432977544437520293L;
	private List<Preadvise> preadvise;
	private Integer id;
	private Boolean respond;
	private String state;
	private int blackboxID;
	private Integer centralID;

	public List<Preadvise> getPreadvise() {
		return preadvise;
	}

	public void setPreadvise(List<Preadvise> preadvise) {
		this.preadvise = preadvise;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Boolean getRespond() {
		return respond;
	}

	public void setRespond(Boolean respond) {
		this.respond = respond;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public int getBlackboxID() {
		return blackboxID;
	}

	public void setBlackboxID(int blackboxID) {
		this.blackboxID = blackboxID;
	}

	public Integer getCentralID() {
		return centralID;
	}

	public void setCentralID(Integer centralID) {
		this.centralID = centralID;
	}
}
