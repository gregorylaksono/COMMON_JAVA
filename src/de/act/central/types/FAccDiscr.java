package de.act.central.types;

/**
 * @author Henry
 */
public class FAccDiscr extends Central implements java.io.Serializable {

	private static final long serialVersionUID = -5969122205323668072L;
	transient private Long accDiscrId;
	private FAccHeader FAccHeader;
	private String ap3lc;
	private Integer part;
	private String discrForm;
	private Integer discrId;
	private String discrRem;

	public FAccDiscr() {
		
	}

	public FAccDiscr(Long accDiscrId) {
		this.accDiscrId = accDiscrId;
	}

	public FAccDiscr(Long accDiscrId, FAccHeader FAccHeader, String ap3lc,
			Integer part, String discrForm, Integer discrId, String discrRem) {
		this.accDiscrId = accDiscrId;
		this.FAccHeader = FAccHeader;
		this.ap3lc = ap3lc;
		this.part = part;
		this.discrForm = discrForm;
		this.discrId = discrId;
		this.discrRem = discrRem;
	}

	public Long getAccDiscrId() {
		return this.accDiscrId;
	}

	public void setAccDiscrId(long accDiscrId) {
		this.accDiscrId = accDiscrId;
	}

	public FAccHeader getFAccHeader() {
		return this.FAccHeader;
	}

	public void setFAccHeader(FAccHeader FAccHeader) {
		this.FAccHeader = FAccHeader;
	}

	public String getAp3lc() {
		return this.ap3lc;
	}

	public void setAp3lc(String ap3lc) {
		this.ap3lc = ap3lc;
	}

	public Integer getPart() {
		return this.part;
	}

	public void setPart(Integer part) {
		this.part = part;
	}

	public String getDiscrForm() {
		return this.discrForm;
	}

	public void setDiscrForm(String discrForm) {
		this.discrForm = discrForm;
	}

	public Integer getDiscrId() {
		return this.discrId;
	}

	public void setDiscrId(Integer discrId) {
		this.discrId = discrId;
	}

	public String getDiscrRem() {
		return this.discrRem;
	}

	public void setDiscrRem(String discrRem) {
		this.discrRem = discrRem;
	}
}
